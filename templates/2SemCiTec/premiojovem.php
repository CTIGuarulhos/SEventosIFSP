<P  STYLE="margin-bottom: 0.35cm"><B>DIÁRIO DE BORDO</B></P>
<OL>
    <LI><P  STYLE="margin-bottom: 0.35cm">O Diário de
            Bordo pode ser manuscrito ou não.</P>
    <LI><P  STYLE="margin-bottom: 0.35cm">O Diário de
            Bordo deve destacar apenas o que foi realizado pelo autor (ou
            autores). E conter o seguinte:</P>
        <OL type="a">
            <LI><P  STYLE="margin-bottom: 0.35cm">Explicação de
                    como e quando surgiu a idéia do trabalho.</P>
            <LI><P  STYLE="margin-bottom: 0.35cm">Descrição de
                    nomes das pessoas com quem conversou ou tirou dúvidas.</P>
            <LI><P  STYLE="margin-bottom: 0.35cm">Descrição das
                    dificuldades e facilidades que encontrou para fazer o trabalho.</P>
            <LI><P  STYLE="margin-bottom: 0.35cm">Descrição dos
                    lugares que visitou e onde realizou a pesquisa.</P>
            <LI><P  STYLE="margin-bottom: 0.35cm">Descrição das

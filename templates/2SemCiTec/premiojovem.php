<H1>ENVIO DE TRABALHOS: JOVEM INOVADOR</H1>
<br><br>
<P STYLE="margin-bottom: 0.35cm"><b>NORMAS</b></P>
<P  STYLE="margin-bottom: 0.35cm">Poderão concorrer
    ao “Prêmio Jovem Inovador” alunos da educação básica (ensino
    fundamental II, ensino médio, ensino técnico, EJA) e jovens entre
    15 e 29 anos, não vinculados a instituições de ensino. Para
    concorrer ao Prêmio Jovem Inovador, os trabalhos deverão ser
    enviados na forma de RESUMO, conforme orientação a seguir. 
</P>
<br><br>
<P  STYLE="margin-bottom: 0.35cm"><B>REGRAS GERAIS</B></P>
<P  STYLE="margin-bottom: 0.35cm">1. Terão direito a
    concorrer ao “Prêmio Jovem Inovador” alunos da educação básica
    (ensino fundamental II, ensino médio, ensino técnico, EJA) e jovens
    entre 15 e 29 anos, não vinculados a instituições de ensino. 
</P>
<P  STYLE="margin-bottom: 0.35cm">2. A inscrição de
    trabalhos deverá ser feita através do site
    <A HREF="http://semcitec.ifspguarulhos.edu.br/"><FONT COLOR="#0000ff"><U>http://semcitec.ifspguarulhos.edu.br/</U></FONT></A>,
    no período lá indicado.</P>
<P  STYLE="margin-bottom: 0.35cm">3. Os trabalhos
    poderão ser desenvolvidos de maneira individual ou em grupo e ter
    relação com o eixo, “Ciência, Saúde, Esporte”, descrito a
    seguir.</P>
<P  STYLE="margin-bottom: 0.35cm">4. Também poderão
    ser apresentados trabalhos que se destacaram em termos de inovação
    em outros concursos ou feiras de ciências. 
</P>
<P  STYLE="margin-bottom: 0.35cm">5. Os trabalhos
    podem ser teóricos ou práticos com apresentação de protótipos.</P>
<P  STYLE="margin-bottom: 0.35cm">6. Os trabalhos
    devem ser submetidos no formato de RESUMO, com no mínimo uma e no
    máximo três páginas, conforme padrão a seguir.</P>
<P  STYLE="margin-bottom: 0.35cm">7. No caso de alunos
    da educação básica, os trabalhos poderão ser orientados por um ou
    mais professores da escola na qual o autor (ou autores) se encontra
    matriculado.</P>
<P  STYLE="margin-bottom: 0.35cm">8. A apresentação
    dos trabalhos deve ser feita pelos próprios autores, na data e hora
    publicadas no site do evento: <A HREF="http://semcitec.ifspguarulhos.edu.br/"><FONT COLOR="#0000ff"><U>http://semcitec.ifspguarulhos.edu.br</U></FONT></A>.
    Os alunos poderão contar com o auxilio dos professores-orientadores.</P>
<P  STYLE="margin-bottom: 0.35cm">9. Na exposição
    dos trabalhos, os autores devem ter à mão o DIÁRIO DE BORDO (Ver
    orientação a seguir).</P>
<P  STYLE="margin-bottom: 0.35cm">10. Os trabalhos
    selecionados serão divulgados no site da 2ª SEMCITEC.</P>
<P  STYLE="margin-bottom: 0.35cm">11. Serão premiados
    os melhores trabalhos da categoria alunos da educação básica por
    modalidade de ensino e os melhores trabalhos da categoria jovens sem
    vínculo escolar.</P>
<P  STYLE="margin-bottom: 0.35cm">12. Serão avaliados
    somente os trabalhos inscritos na forma de resumo, conforme padrão a
    seguir.</P>
<P  STYLE="margin-bottom: 0.35cm">13. A organização
    da SEMCITEC reserva-se o direito de modificar ou ajustar a qualquer
    momento as regras acima.</P>
<br><br>
<P  STYLE="margin-bottom: 0.35cm"><B>EIXO TEMÁTICO</B></P>
<P  STYLE="margin-bottom: 0.35cm"><B>Ciência, Saúde,
        Esporte</B></P>
<br><br>
<P  STYLE="margin-bottom: 0.35cm">O eixo abrange
    trabalhos voltados para:</P>
<OL>
    <LI><P  STYLE="margin-bottom: 0.35cm">a geração e
            difusão de inovações no campo da saúde e do esporte;</P>
    <LI><P  STYLE="margin-bottom: 0.35cm">o
            desenvolvimento ou aperfeiçoamento de procedimentos, produtos e
            equipamentos destinados à manutenção ou reabilitação da saúde
            do ser humano e dos animais; 
        </P>
    <LI><P  STYLE="margin-bottom: 0.35cm">o
            desenvolvimento ou o aperfeiçoamento de procedimentos, produtos ou
            equipamentos sanitários, visando o bem-estar da população e a
            preservação do meio ambiente;</P>
    <LI><P  STYLE="margin-bottom: 0.35cm">o
            desenvolvimento de jogos recreativos, virtuais ou não; 
        </P>
    <LI><P  STYLE="margin-bottom: 0.35cm">o
            desenvolvimento de procedimentos, produtos, equipamentos voltados
            para a melhoria e à elevação do bem-estar da população, em
            especial de crianças, adolescentes, idosos e pessoas com
            deficiência física.</P>
</OL>

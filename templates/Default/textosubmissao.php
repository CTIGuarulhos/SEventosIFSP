        <p align="CENTER">
            <b>NORMAS PARA INSCRI&Ccedil;&Atilde;O E SUBMISS&Atilde;O DE TRABALHOS</b></p>
        <p>
            &nbsp;</p>
        <p>
            A submiss&atilde;o de trabalhos poder&aacute; ser realizada por qualquer participante, tanto na categoria de relato de experi&ecirc;ncia como na categoria de pesquisa acad&ecirc;mica conclu&iacute;da ou em andamento, desde que atenda os crit&eacute;rios estabelecidos e descritos a seguir.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>CRIT&Eacute;RIOS PARA A SELE&Ccedil;&Atilde;O</strong></p>
        <p>
            &nbsp;</p>
        <ol>
            <li>
                <p>
                    Os trabalhos devem ter pertin&ecirc;ncia ao tema do evento e inserido em um dos eixos propostos.</p>
            </li>
            <li>
                <p>
                    Autores e co-autores dever&atilde;o estar inscritos no evento.</p>
            </li>
            <li>
                <p>
                    Cada participante poder&aacute; inscrever apenas um trabalho como autor.</p>
            </li>
            <li>
                <p>
                    Os trabalhos devem atender &agrave;s normas especificadas no item Submiss&atilde;o de Trabalhos.</p>
            </li>
        </ol>
        <p>
            &nbsp;</p>
        <p>
            <strong>EIXOS TEM&Aacute;TICOS PARA SUBMISS&Atilde;O DE TRABALHOS</strong></p>
        <p>
            &nbsp;</p>
        <p>
            Leia atentamente as ementas dos Eixos Tem&aacute;ticos para saber em qual eixo o seu trabalho se enquadra. &Eacute; poss&iacute;vel que seu trabalho se enquadre em mais de um Eixo Tem&aacute;tico. Entretanto, voc&ecirc; deve optar por apenas um.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>Eixo Tem&aacute;tico: Ci&ecirc;ncia, Inova&ccedil;&atilde;o e Educa&ccedil;&atilde;o</strong></p>
        <p>
            Este&nbsp;eixo comporta estudos de abordagem te&oacute;rica ou decorrentes de pr&aacute;ticas pedag&oacute;gicas vivenciadas em ambientes de ensino que contribuam: para a gera&ccedil;&atilde;o e difus&atilde;o de pr&aacute;ticas de ensino e aprendizagem inovadoras; para a forma&ccedil;&atilde;o inicial e continuada de professores e profissionais de todos os n&iacute;veis e &nbsp;modalidades de educa&ccedil;&atilde;o; para o aperfei&ccedil;oamento dos instrumentos de gest&atilde;o participativos e democr&aacute;ticos pela comunidade escolar e de seu entorno; para o aperfei&ccedil;oamento da organiza&ccedil;&atilde;o curricular e eleva&ccedil;&atilde;o da qualidade do ensino e da aprendizagem de todos os n&iacute;veis e &nbsp;modalidades de educa&ccedil;&atilde;o.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>Eixo Tem&aacute;tico: Ci&ecirc;ncia e Inova&ccedil;&atilde;o em Sa&uacute;de</strong></p>
        <p>
            O eixo abrange trabalhos resultantes de investiga&ccedil;&atilde;o te&oacute;rica ou pr&aacute;tica que contribuam&nbsp;para&nbsp;a amplia&ccedil;&atilde;o do conhecimento&nbsp;aplicado &agrave; &aacute;rea de sa&uacute;de, para&nbsp;a gera&ccedil;&atilde;o e difus&atilde;o de inova&ccedil;&otilde;es no campo da sa&uacute;de, para a forma&ccedil;&atilde;o cont&iacute;nua dos profissionais da &aacute;rea, para o desenvolvimento de produtos e equipamentos, para o fortalecimento do setor produtivo p&uacute;blico e privado, tendo como par&acirc;metros a relev&acirc;ncia social, sanit&aacute;ria e econ&ocirc;mica dos conhecimentos produzidos e o controle social.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>Eixo Tem&aacute;tico:&nbsp;Ci&ecirc;ncia e Inova&ccedil;&atilde;o em Esporte</strong></p>
        <p>
            Este eixo comporta trabalhos resultantes de investiga&ccedil;&atilde;o te&oacute;rica ou pr&aacute;tica, relacionados: ao aperfei&ccedil;oamento, &agrave; manuten&ccedil;&atilde;o ou reabilita&ccedil;&atilde;o da sa&uacute;de do ser humano; &agrave; gera&ccedil;&atilde;o e difus&atilde;o de inova&ccedil;&otilde;es no campo do esporte; ao esporte como direito social, com aten&ccedil;&atilde;o especial a crian&ccedil;as, adolescentes, idosos e pessoas com defici&ecirc;ncia f&iacute;sica; ao significado hist&oacute;rico do esporte de alto rendimento e seu impacto na sociedade brasileira; a forma&ccedil;&atilde;o de profissionais da &aacute;rea e &agrave; avalia&ccedil;&atilde;o de pol&iacute;ticas p&uacute;blicas para o esporte.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>Eixo Tem&aacute;tico: Pr&aacute;ticas Locais Sustent&aacute;veis:</strong></p>
        <p>
            Este eixo&nbsp;abrange trabalhos de investiga&ccedil;&atilde;o de m&eacute;todos e pr&aacute;ticas locais, voltados &agrave; promo&ccedil;&atilde;o da sa&uacute;de e eleva&ccedil;&atilde;o do bem estar da popula&ccedil;&atilde;o de Guarulhos, podendo comportar an&aacute;lise de pol&iacute;ticas que promovam o desenvolvimento econ&ocirc;mico e social sustentado, a participa&ccedil;&atilde;o e o controle social, nas diversas &aacute;reas de interesse: educa&ccedil;&atilde;o, sa&uacute;de, habita&ccedil;&atilde;o, saneamento b&aacute;sico, esporte e lazer, entre outras.&nbsp;</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>SUBMISS&Atilde;O DE TRABALHOS</strong></p>
        <p>
            &nbsp;</p>
        <p>
            A II SEMCITEC aceita a submiss&atilde;o de trabalhos nas seguintes categorias:</p>
        <p>
            &nbsp;</p>
        <ol>
            <li>
                <p>
                    <strong>Pesquisa cient&iacute;fica</strong>: nesta categoria ser&atilde;o aceitas apresenta&ccedil;&otilde;es de resultados parciais ou finais de pesquisas que versem sobre temas inseridos em um dos eixos tem&aacute;ticos propostos. Nesta categoria, cabem trabalhos de natureza te&oacute;rica e emp&iacute;rica que busquem articula&ccedil;&atilde;o com o tema central do encontro. &Eacute; importante ressaltar a necessidade do trabalho apresentar resultados parciais ou finais.</p>
            </li>
            <li>
                <p>
                    <strong>Relato de experi&ecirc;ncia</strong>: Nesta categoria, o trabalho deve apresentar uma reflex&atilde;o sobre uma a&ccedil;&atilde;o, ou conjunto de a&ccedil;&otilde;es, que versem sobre temas inseridos em um dos eixos tem&aacute;ticos propostos. &Eacute; importante que o texto contemple uma descri&ccedil;&atilde;o detalhada do desenvolvimento da experi&ecirc;ncia com observa&ccedil;&otilde;es e reflex&otilde;es do autor.</p>
            </li>
        </ol>
        <p>
            &nbsp;</p>
        <p>
            Para qualquer uma das categorias descritas, os trabalhos dever&atilde;o ser submetidos no formato de <strong>resumo expandido</strong>, estruturado com no <strong>m&iacute;nimo 3 (tr&ecirc;s)</strong> e no <strong>m&aacute;ximo 5 (cinco) p&aacute;ginas</strong>, incluindo as refer&ecirc;ncias bibliogr&aacute;ficas. Para ambas as modalidades ser&aacute; disponibilizado um &ldquo;arquivo padr&atilde;o&rdquo;, no link &ldquo;Modelo para Submiss&atilde;o de Trabalhos&rdquo;, contendo as orienta&ccedil;&otilde;es para os textos.</p>
        <p>
            &nbsp;</p>
        <p>
            A digita&ccedil;&atilde;o e formata&ccedil;&atilde;o do resumo expandido devem obedecer &agrave;s seguintes orienta&ccedil;&otilde;es:</p>
        <ul>
            <li>
                <p>
                    Fonte: Times New Roman, tamanho 12;</p>
            </li>
            <li>
                <p>
                    Papel tamanho A4;</p>
            </li>
            <li>
                <p>
                    Margem superior e inferior com 2,5 cm;</p>
            </li>
            <li>
                <p>
                    Margem esquerda e direita com 3 cm;</p>
            </li>
            <li>
                <p>
                    Espa&ccedil;amento entre linhas: simples;</p>
            </li>
            <li>
                <p>
                    Alinhamento: Justificado;</p>
            </li>
            <li>
                <p>
                    T&iacute;tulo em mai&uacute;sculo/negrito com alinhamento centralizado;</p>
            </li>
            <li>
                <p>
                    Tr&ecirc;s a cinco palavras-chave.</p>
            </li>
        </ul>
        <p>
            &nbsp;</p>
        <p>
            No momento do envio do trabalho, o autor dever&aacute; escolher a forma de apresenta&ccedil;&atilde;o que lhe interessa. Os trabalhos poder&atilde;o ser apresentados nas seguintes modalidades: (a) Apresenta&ccedil;&atilde;o de p&ocirc;steres ou (b) Comunica&ccedil;&atilde;o oral, conforme descri&ccedil;&atilde;o a seguir:</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>(a) APRESENTA&Ccedil;&Atilde;O DE P&Ocirc;STERES</strong></p>
        <p>
            Os interessados em apresentar trabalhos nesta modalidade dever&atilde;o observar as seguintes orienta&ccedil;&otilde;es:</p>
        <ol>
            <li>
                <p>
                    O p&ocirc;ster dever&aacute; ter as seguintes dimens&otilde;es: 90 cm de largura por 120 cm de altura e obedecer &agrave; seguinte formata&ccedil;&atilde;o:</p>
            </li>
        </ol>
        <ul>
            <li>
                <p>
                    Textos leg&iacute;veis a uma dist&acirc;ncia de, pelo menos, 1 m.</p>
            </li>
            <li>
                <p>
                    Tipo de letra &ldquo;Arial&rdquo;, no tamanho 36 para o t&iacute;tulo de se&ccedil;&otilde;es, 27 para subt&iacute;tulos e 24 para textos e resumos.</p>
            </li>
            <li>
                <p>
                    Legenda de gr&aacute;ficos e imagens com tamanho da letra n&atilde;o inferior a 12.</p>
            </li>
            <li>
                <p>
                    Conter no cabe&ccedil;alho: t&iacute;tulo, nomes dos autores, institui&ccedil;&atilde;o de origem, endere&ccedil;os eletr&ocirc;nicos. E no corpo do trabalho: introdu&ccedil;&atilde;o, objetivos, metodologia, resultados e/ou reflex&otilde;es e refer&ecirc;ncias bibliogr&aacute;ficas, se for o caso, conforme as normas da ABNT.</p>
            </li>
            <li>
                <p>
                    Figuras, fotos, esquemas e gr&aacute;ficos devem ter boa resolu&ccedil;&atilde;o (1028 x 1028 pixels), impresso em papel couch&ecirc;, 240g.</p>
            </li>
            <li>
                <p>
                    T&iacute;tulo deve ser o mesmo do resumo expandido, submetido &agrave; organiza&ccedil;&atilde;o do evento.</p>
            </li>
            <li>
                <p>
                    Conter logotipo da institui&ccedil;&atilde;o, do evento e de ag&ecirc;ncia de fomento, se houver.</p>
            </li>
            <li>
                <p>
                    Conter cord&atilde;o para permitir sua fixa&ccedil;&atilde;o.</p>
            </li>
        </ul>
        <p>
            &nbsp;</p>
        <ol start="2">
            <li>
                <p>
                    N&atilde;o ser&atilde;o aceitos p&ocirc;steres que n&atilde;o estejam de acordo com o padr&atilde;o acima.</p>
            </li>
            <li>
                <p>
                    A apresenta&ccedil;&atilde;o dos p&ocirc;steres ser&aacute; realizada durante a sess&atilde;o destinada a esse fim.</p>
            </li>
            <li>
                <p>
                    Clique <a href="<?php echo $CONFIG->URL_ROOT . "/templates/" . $EVENTO['TEMPLATE'] . "/docs/Template_Poster1.ppt" ?>"><strong><span style="color:#ff0000;">aqui</span></strong></a> para acessar o modelo padr&atilde;o do p&ocirc;ster.</p>
            </li>
        </ol>
        <p>
            &nbsp;</p>
        <p>
            <strong>(b) COMUNICA&Ccedil;&Atilde;O ORAL</strong></p>
        <p>
            Os interessados poder&atilde;o submeter trabalhos para apresenta&ccedil;&atilde;o oral em sess&otilde;es de Comunica&ccedil;&atilde;o Oral, sendo a dura&ccedil;&atilde;o de cada apresenta&ccedil;&atilde;o de 15min. O evento contempla estas apresenta&ccedil;&otilde;es havendo sess&otilde;es paralelas para acomodar os trabalhos aceitos. Nesta modalidade ser&atilde;o recebidos at&eacute; 50 trabalhos, sendo que apenas os aprovados ir&atilde;o para Comunica&ccedil;&atilde;o Oral.</p>
        <p>
            Os autores que n&atilde;o conseguirem se inscrever na modalidade Comunica&ccedil;&atilde;o Oral, poder&atilde;o inscrever seus trabalhos na modalidade p&ocirc;ster.</p>
        <p>
            &nbsp;</p>
        <p>
            <strong>AVALIA&Ccedil;&Atilde;O DOS TRABALHOS INSCRITOS</strong></p>
        <ol>
            <li>
                <p>
                    Os trabalhos que atendam &agrave;s orienta&ccedil;&otilde;es de formula&ccedil;&atilde;o e formata&ccedil;&atilde;o ser&atilde;o encaminhados, sem identifica&ccedil;&atilde;o de autoria, ao Comit&ecirc; de Avalia&ccedil;&atilde;o Cient&iacute;fica, que selecionar&aacute; os trabalhos por Eixo Tem&aacute;tico para serem apresentados na modalidade de p&ocirc;ster ou comunica&ccedil;&atilde;o oral.</p>
            </li>
        </ol>
        <p>
            O resultado da avalia&ccedil;&atilde;o ser&aacute; publicado &ntilde;o m&iacute;nimo um m&ecirc;s antes da realiza&ccedil;&atilde;o do evento.</p>
        <p>
            &nbsp;</p>
        <p>

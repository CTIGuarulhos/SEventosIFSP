            </li>
            <li>
                <p>
                    Aos apresentadores de trabalhos tamb&eacute;m ser&atilde;o fornecidos <strong>certificados</strong>, mediante inscri&ccedil;&atilde;o no evento, aprova&ccedil;&atilde;o do trabalho pelo comit&ecirc; cient&iacute;fico, e comparecimento para apresenta&ccedil;&atilde;o do trabalho.</p>
